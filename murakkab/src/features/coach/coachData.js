import { fmt } from '../../shared/utils/format';

export function classify(scen, sim, hiddenMode) {
  const fy = sim.final, fb = sim.salemFinal;
  const sells = sim.receipts.filter(r => r.act === 'sell');
  const N = scen.prices.length;

  let panic = false;
  let runMax = scen.prices[0];
  const maxAt = [];
  for (let i = 0; i < N; i++) { runMax = Math.max(runMax, scen.prices[i]); maxAt.push(runMax); }
  sells.forEach(r => { if (r.act === 'sell' && r.f >= 0.5 && r.price < maxAt[r.m] * 0.85) panic = true; });

  const rets = [];
  for (let i = 1; i < N; i++) rets.push({ i, r: scen.prices[i] / scen.prices[i - 1] - 1 });
  rets.sort((a, b) => b.r - a.r);
  const missed = rets.slice(0, 10).filter(t => sim.outMonths.has(t.i)).length;
  const flags = [sim.dcaOff <= N * 0.2, !panic, missed <= 3];
  const why = [
    flags[0] ? 'واصلت استثمار راتبك' : 'وقّفت راتبك عن الاستثمار فترة طويلة',
    flags[1] ? 'ما بعت وقت الذعر' : 'بعت أثناء هبوط حاد (بيع ذعر)',
    flags[2] ? 'حضرت أفضل الشهور' : 'فاتتك أفضل شهور التعافي',
  ];
  const st = { stars: flags.filter(Boolean).length, flags, why, panic, missed };

  if (scen.id === 'crash2006') {
    if (sells.length === 0 && sim.dcaOff <= N * 0.2) return { key: 'twin2006', fy, fb, st };
    if (fy > fb && sells.length) return { key: 'escape2006', fy, fb, st, sellM: sells[0].m };
    const rb = sim.receipts.find(r => r.act === 'invest');
    if (rb && sells.length && rb.price > sells[0].price) return { key: 'chase', fy, fb, st };
    if (sells.length) return { key: 'panic2006', fy, fb, st, sellM: sells[0].m };
    return { key: 'dcastop', fy, fb, st };
  }

  if (sells.length === 0 && sim.dcaOff <= N * 0.2) return { key: 'twin', fy, fb, st };
  if (sells.length === 0) return { key: 'dcastop', fy, fb, st };

  let peakM = 0;
  for (let i = 0; i < N; i++) if (scen.prices[i] > scen.prices[peakM]) peakM = i;
  const first = sells[0];
  if (st.panic && fy < fb) return { key: 'panic', fy, fb, st, sellM: first.m };
  if (Math.abs(first.m - peakM) <= 2 && fy > fb) return { key: 'lucky', fy, fb, st, sellM: first.m };
  const rebuy = sim.receipts.find(r => r.act === 'invest');
  if (rebuy && rebuy.price > first.price) return { key: 'chase', fy, fb, st };
  if (fy > fb) return { key: 'winner', fy, fb, st };
  return { key: 'mixed', fy, fb, st };
}

